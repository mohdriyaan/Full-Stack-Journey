// for..of loop:- A nice and easy way of iterating over arrays or other iterable objects

const subreddits = ["cringe","books","funny","pics"]

// Syntax:- 
/* 
for(variable of iterable){
    statement
}
*/

for(let subs of subreddits){
    console.log(subs)
}

for(let chars of "hello world"){
    console.log(chars)
}

